#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*cdest;
	char	*csrc;
	size_t	i;

	if (!dst && !src)
		return (NULL);
	cdest = (char *)dst;
	csrc = (char *)src;
	i = 0;
	while (i < n)
	{
		cdest[i] = csrc[i];
		i++;
	}
	return (dst);
}
